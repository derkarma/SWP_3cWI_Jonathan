package at.jonathan.objectOrientation.ISaHASa;

public class Animal {

    private int weigth;
    private int height;

    public Animal(int weigth, int height) {
        this.weigth = weigth;
        this.height = height;
    }

    public int getWeigth() {
        return weigth;
    }

    public void setWeigth(int weigth) {
        this.weigth = weigth;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
